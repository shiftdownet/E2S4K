function main() {
    if ( $pu_path -eq "" ) {
        Write-Host "Aruguments are invalid. Please refer to the following"
        Write-Host "  -pu_path [FileName]"
    }
    else {
        $jar_path = (Join-Path $PSScriptRoot "./plantuml-jar-mit-1.2022.5/plantuml.jar" )
        java -jar $jar_path -tsvg $pu_path
    }
}

